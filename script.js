//first letter of a string uppercase, but not change the case of any of the other letters
function capitalize (apaaja) {
    return apaaja.charAt(0).toUpperCase() + apaaja.slice(1)
}


//takes a string and returns the very last letter of that string: 
function lastLetter (last) {
     return last.slice(-1)
}